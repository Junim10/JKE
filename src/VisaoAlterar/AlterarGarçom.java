    private void jButton5ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton5ActionPerformed
        String codigo = jTextField1.getText();
        String nome = jTextField2.getText();
        String tel = jTextField3.getText();
        String end = jTextField4.getText();

        if( nome.equalsIgnoreCase("") || tel.equalsIgnoreCase("") || end.equalsIgnoreCase("") ){
            JOptionPane.showMessageDialog(null, "Nenhum campo pode estar vazio", "Video Locadora", JOptionPane.WARNING_MESSAGE);
        }else{
            Connection con = ConexaoDAO.AbrirConexao();
            GarçomDAO sql = new GarçomDAO(con);
            int cod = Integer.parseInt(codigo);
            garçom c = new garçom();

            c.setCod(cod);
            c.setNome(nome);
             c.setTelefone(tel);
            c.setEndereco(end);

            sql.Alterar_Garçom(c);
            ConexaoDAO.FecharConexao(con);

            jTextField1.setText("");
            jTextField2.setText("");
            jTextField3.setText("");
            jTextField4.setText("");

            JOptionPane.showMessageDialog(null, "Alteração realizada com sucesso!", "Video Locadora", JOptionPane.INFORMATION_MESSAGE);
            dispose();
        }
    }//GEN-LAST:event_jButton5ActionPerformed

