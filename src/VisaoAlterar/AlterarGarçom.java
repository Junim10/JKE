   private void InserirDados(int cod){
        Connection con = ConexaoDAO.AbrirConexao();
        GarçomDAO sql = new GarçomDAO(con);
        List<garçom> lista = new ArrayList<>();
        lista = sql.CapturarGarçom(cod);
        
        for(garçom c : lista){
            jTextField1.setText("" + c.getCod());
            jTextField2.setText("" + c.getNome());
            jTextField3.setText("" + c.getTelefone());
            jTextField4.setText("" + c.getEndereco());

        }
        ConexaoDAO.FecharConexao(con);
    }
